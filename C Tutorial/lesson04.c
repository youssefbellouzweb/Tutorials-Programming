#include<stdio.h>

/* 
====Arithmatic Operator:====
    Addition +
    Subtraction -
    Multiplication *
    Division /
    Remainder %

    / * first
    - + after
*/

void main() {
    int x = 5;
    int y = 2;
    int result = x + y;

    printf("x + y => %d \n", result);
    printf("x + y => %d \n", x + y);
    printf("x - y => %d \n", x - y);
    printf("x * y => %d \n", x * y);
    printf("x / y => %d \n", x / y);
    printf("x %% y => %d \n", x % y); // %% in print %
}